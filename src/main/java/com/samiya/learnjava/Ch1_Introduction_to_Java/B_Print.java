package main.java.com.samiya.learnjava.Ch1_Introduction_to_Java;

public class B_Print {
    public static void main(String[] args) {
        // Print statements to display messages
        System.out.println("Welcome to Java Programming!");
        System.out.println("I'm excited to explore Java's features.");
        System.out.println("Let's begin our journey!");

        // The print() method does not insert a new line at the end of the output
        System.out.print("Hello World! ");
        System.out.print("This is a demonstration of the print() method, ");
        System.out.print("which prints text on the same line.");

        // Print numbers and expressions
        System.out.println(3);
        System.out.println(358);
        System.out.println(50000);

        // Print the result of mathematical expressions
        System.out.println("3 + 3 = " + (3 + 3)); // Addition
        System.out.println("2 * 5 = " + (2 * 5)); // Multiplication
    }
}
